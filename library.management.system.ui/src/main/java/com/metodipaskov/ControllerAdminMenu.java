package com.metodipaskov;

import javafx.event.ActionEvent;
import javafx.fxml.FXMLLoader;
import javafx.scene.Node;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

import java.io.IOException;

public class ControllerAdminMenu {

    private Stage stage;
    private Scene scene;
    private Parent root;

    public void updateClerk(ActionEvent event) throws IOException {
        root = FXMLLoader.load(getClass().getResource("updateClerk.fxml"));
        stage = (Stage) ((Node) event.getSource()).getScene().getWindow();
        scene = new Scene(root);
        stage.setScene(scene);
        stage.show();
    }

    public void viewAllUsers(ActionEvent event) throws IOException {
        root = FXMLLoader.load(getClass().getResource("allUsers.fxml"));
        stage = (Stage) ((Node) event.getSource()).getScene().getWindow();
        scene = new Scene(root);
        stage.setScene(scene);
        stage.show();
    }

    public void viewAllIssuedBooks(ActionEvent event) throws IOException {
        root = FXMLLoader.load(getClass().getResource("allissuedBooks.fxml"));
        stage = (Stage) ((Node) event.getSource()).getScene().getWindow();
        scene = new Scene(root);
        stage.setScene(scene);
        stage.show();
    }

    public void viewAllBooks(ActionEvent event) throws IOException {
        root = FXMLLoader.load(getClass().getResource("viewAllBooks.fxml"));
        stage = (Stage) ((Node) event.getSource()).getScene().getWindow();
        scene = new Scene(root);
        stage.setScene(scene);
        stage.show();
    }

    public void logOut(ActionEvent event) throws IOException {
        root = FXMLLoader.load(getClass().getResource("main.fxml"));
        stage = (Stage) ((Node) event.getSource()).getScene().getWindow();
        scene = new Scene(root);
        stage.setScene(scene);
        stage.show();
    }

}
